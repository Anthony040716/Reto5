    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        String username = txtUsername.getText();
        String password = txtPassword.getText();
        //Mostramos valores 
        System.out.println("Username" + username + "Password " + password);
        //Validar que los campos no estan vacios
        if (username.isEmpty() || password.isEmpty()) {
            //JOptionPane.showMessageDialog(null,"Falatan Campos por diligenciar
            JOptionPane.showMessageDialog(null, "Hay Campos Sin Digilenciar", "Informacion", JOptionPane.WARNING_MESSAGE);

        } else {
            if (username.equals("MisionTIC") && password.equals("Ciclo2")) {
                JOptionPane.showMessageDialog(null, "Bienvenido al Sistema de Mision TIC");
                // Hacer Visible el nuevo JFRAME
                UserMenu usermenu = new UserMenu();
                usermenu.setVisible(true);
                //Eliminamos El Frame de login para que no se muestre 
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al Iniciar Sesion", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

